export function useCabins() {
  const getAllCabins = (): Cabin[] => cabins

  const getCabinBySlug = (slug: string): Cabin | undefined =>
    cabins.find((c) => c.slug === slug)

  const getFeaturedCabins = (count = 3): Cabin[] => cabins.slice(0, count)

  return {
    getAllCabins,
    getCabinBySlug,
    getFeaturedCabins,
  }
}
