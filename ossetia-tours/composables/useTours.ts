export function useTours() {
  const getAllTours = (): Tour[] => tours

  const getTourBySlug = (slug: string): Tour | undefined =>
    tours.find((t) => t.slug === slug)

  const getFeaturedTours = (count = 3): Tour[] => tours.slice(0, count)

  return { getAllTours, getTourBySlug, getFeaturedTours }
}
